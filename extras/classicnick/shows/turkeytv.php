         <p align="center"><img src="turkeytv1.gif" width="160" height="120"></p>
         <p align="justify"><em>Turkey TV</em> was a Canadian-produced sketch comedy for youth, ordered by Nickelodeon due to <em>You Can't Do That on Television</em>'s popularity. It featured many original skits and comedic selections, including many clips from NJ's infamous Uncle Willy and Floyd Show.</p>
         <p align="justify">The series was about an animated Turkey, who traveled around the world filming television shows from other countries, then &quot;bringin' it home to Hollywood and puttin' it on the air&quot;. The cast featured Les Lye, Christine McGlade, Kevin Kubusheskie, and Adam Reid, all of You Can't Do That on Television fame.</p>
         <p align="justify">Included in the segments were &quot;A Day in the Life of Food&quot;, &quot;Hillary and Katie&quot;, &quot;Gramps and Sonny&quot;, a segment where Adam Reid talks to an alien who lives in his closet, and all we see is the alien's hand, &quot;Good Ol' Kentucky Boys&quot;, and many more. The show aired on both Nickelodeon and Nick-At-Nite. </p>
