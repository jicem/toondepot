         <p align="justify"><em>Mapletown</em> originally aired in Japan. It was about a village inhabited by animals who were like humans. The story centered on the Rabbit family (Patty, Ricky, Rachael, and their parents), Patty Rabbit's best friend Bobby Bear, and assorted other inhabitants of the town (Mayor Dandy-Lion, Danny Dog, Fanny Fox, Miss Dear, Sheriff Bulldog, and the infamous Wild Wolf).</p>
         <p align="justify">The show always started out at Mrs. Maple's house, and she would explain the plot of the days episode. It always closed there as well, and she would almost always say: &quot;My garden of Maple Town is very special to me.. but each and everyone of you is even more special.&quot; She was definitely a motherly image.</p>
