         <p align="justify"><em>Lance Link: Secret Chimp</em> was a show like no other. It was basically a police action type show that used chimps (and one orangutan) as the characters. I personally remember very little about it.</p>
         <p align="justify">Lance Link originally aired following Get Smart, and was even more off the wall than that show. It pitted the goods guys of A.P.E. (Agency to Prevent Evil) against the bad guys from C.H.U.M.P. (Criminal Headquarters for Underworld Master Plan), which was led by the evil Baron von Butcher. Lance Link was the head of APE.</p>
         <p align="justify">The first season featured hour long episodes that included old Daffy Duck cartoons and music from Lance's chimp rock band, The Evolution Revolution. No word on the availability of their latest CD.</p>
