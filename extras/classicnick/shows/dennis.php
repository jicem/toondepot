         <p align="justify">Dennis the Menace first appeared on the small screen in 1959, where he was portrayed by Jay North. Dennis was truly entertaining to watch, because he was always trying to help the residents of his neighborhood (particularly &quot;Good Ole Mr. Wilson&quot;), but always seemed to make matters worse. The results usually kept the viewers laughing.</p>
         <p align="justify">The characters remained mostly the same throughout the series, the only notably change was that George Wilson's brother John replaced him in 1962. The reason for this was that Joseph Kearns (George Wilson) died midway through the 1961-1962 season.</p>
         <p align="justify">Dennis the Menace is certainly a classic, and deserves to be on TV Land or Nick-At-Nite.</p>
