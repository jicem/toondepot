         <p align="justify"><em>Going Great </em>was a newsmagazine type show that premiered on Nickelodeon in 1983. Hosted by Chris Makepeace, this was the first show Geraldine Laybourne oversaw as president of Nickelodeon. This show focused on kids who did amazing feats, such as a thirteen year old who wrote a bestseller. Despite it's high quality, the show was not successful, and was canned after only thirteen weeks. The reason was that the show depressed kids, who wanted to see shows about what kids normally do, not things that they themselves had never done. When it was released, it seemed like a surefire hit, and Laybourne talked about it for years to represent how unpredictable kids really are. </p>
