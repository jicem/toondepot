         <p align="justify">Flipper is an icon in the animal/human friendship category of pop culture, probably second only to Lassie. Flipper was an excellent show that dealt with the Ricks family, father Porter, and brothers, Sandy and Bud. Porter Ricks was a Ranger at Coral Key Park in Florida. His sons Sandy and Bud had developed a friendship with an intelligent dolphin named Flipper.</p>
         <p align="justify">Of course, being Ranger led into many plots of the story, whether it be fighting evil boaters, or helping beached whales. The Ricks family usually saved the day with a few assists from Flipper.</p>
