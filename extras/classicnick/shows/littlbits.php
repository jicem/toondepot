         <p align="justify"><em>The Littl' Bits</em> debuted on Nickelodeon in 1991, replacing <em>Today's Special</em> on the network. I, for one, wasn't very happy.</p>
         <p align="justify">The Littl' Bits was a cute cartoon that centered on the happenings of a small village (ever notice how cartoons always say 'village', and never say 'city', or 'town') in Foothill Forest. The main characters were the children: Lillabit, Williebit, Snagglebit (his self-steem must have suffered after being given that name). The kids were mostly innocent, except for Snagglebit, who happened to be the mayor's son, and also that spoiled brat every show that deals with kids includes.</p>
         <p align="justify">The adults of the town included the parents (or guardians) of the children: Mayor Bossabit (Snagglebit's father), Snoozabit (Lillabit's guardian), Elderbit (the village elder.. I wonder if he was named Elderbit when he was young?), and the incomparable Browniebit. Oh and who can forget the flying squirrel, and another rodent (was it a mouse? I can't remember).</p>
         <p align="justify">All joking aside, this was really a great show, and one that needs to be aired again.</p>
