         <p align="justify"><em>The World of David the Gnome</em> was based on the world famous children's books <em>The Gnomes</em> and <em>The Secret of the Gnomes</em>, both by Dutch authors Rien Poortvliet and Wil Huygen. It is unclear how many episodes were made. One source says there were 26, and another says 52. I would lean towards 52, but I can't be sure.</p>
         <p align="justify">David the Gnome, and his wife Lisa, where peace loving Gnomes who lived in a tree. They probably reminded more than a few children of their grandparents. David would travel around on the back of his trusty fox, Swift, who he would summon by whistling. Overall it was a very interesting show which taught children to enjoy and respect nature and the world around them.</p>
