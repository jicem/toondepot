         <p align="justify">Under the Mountain was New Zealand in origin, and focused on twins Rachel and Theo who went to visit an aunt. While they were they, they met Mr. Jones, who they had apparently met several years earlier. It turned out that Mr. Jones was an alien, and he had been sent to recruit Rachel and Theo to assist him in his battles with another race of aliens, who were sluglike, but could take on any form. It was also revealed that the twins had psychic abilities, and they were able to use this to defeat the evil aliens. </p>
