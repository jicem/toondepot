         <p align="justify">This show originally aired in the UK, and was centered on bizarre rock formation in England. A scientist named Adam Brake discovered that the entire population of a neighboring town were somehow under the spell of an unseen force originating from the formation. This series lasted for seven episodes, until finally Brake and his son Matthew discover a way to break the bizarre spell. </p>
