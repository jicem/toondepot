         <p align="justify">Helen, Terry, and Phil accidentally released Rothgo, a sorcerer who had been trapped. He then took them on a quest through time and space to recover a stolen artifact that apparently had some huge power behind it. It turned out to be in the clutches of the evil witch Belor. Originally there were three seasons filmed in the UK, but only the first season (seven episodes) crossed the Atlantic to appear on the First Network for Kids. </p>
