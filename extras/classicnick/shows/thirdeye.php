         <p align="justify">This was a classic ghost story. Cassie was the seventh daughter of the seventh daughter, which, due to her lineage, caused her to be able to summon the spirit of Deverill (think about that one). The Palmer's did manage to defeat the evil spirit, however. Ran for seven episodes, from the UK. </p>
