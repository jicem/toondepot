         <p align="justify"><em>The Third Eye</em> was a hodgepodge of foreign mini series' that aired on Nickelodeon beginning in 1983. It consisted of &quot;Into the Labyrinth&quot;, &quot;Children of the Stones,&quot; &quot;The Haunting of Cassie Palmer,&quot; and &quot;Under The Mountain.&quot; These shows were very strange, and not the upbeat stuff kids were used to. In fact, many people have told me they didn't believe these shows were originally aimed at children at all. Their bizarre nature, and unclear target audience made them something of a novelty that hasn't been seen since it's time on Nick. I think they MIGHT have resurfaced on Special Delivery a few years later, but I'm not sure. </p>
