It was Mr. Magoo, intoduced in 1949's <B> Ragtime Bear</b>, that gave UPA (United Productions of America) its first 
popular success. 
 &#160 But it would be <b>Gerald McBoing Boing</B>, 
completed in 1950 and 
released in January 1951 that would solidify the studio's reputation. &#160 
<b>Gerald McBoing Boing</B> is
simple but highly stylized with bold line drawings, understated motion, striking 
color and airy design. &#160 It was a major departure from the classic Disney, Warners and MGM 
cartoons of the day.

<P>
 <b>Gerald McBoing Boing</B> won an Oscar and was greeted with many accolades upon it's debut that resounded for
 several years
and paved the way for other UPA endeavors. &#160 Bobe Cannon directed three follow-ups for UPA:
<B> Gerald McBoing Boing's Symphony </B>(1953), <B>How Now Boing Boing </B>(1954), and 
<b>Gerald McBoing Boing on the Planet Moo </B>(1955), which was nominated for an Oscar.
Gerald would also co-star with Magoo on TV in <b> Magoo Meets McBoing Boing</b>  (1961) and again in <B> Mr. Magoo's Christmas Carol</b>  (1962).</P>
