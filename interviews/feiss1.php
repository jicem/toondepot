<P><FONT size=4>An Interview With David Feiss</FONT></P>
      <h3>Keith Jones: 
        When were you born?<br>
        </h3>
      <h3>David Feiss: 
        April 16th, 1959. I'm 42 years old.</h3>
      <h3>KJ: What 
        do you remember from your childhood?<br>
        </h3>
      <h3>DF: Cartoons 
        dominate my childhood mostly.</h3>
      <h3>KJ: How did 
        you first get into cartoons and animation?<br>
        </h3>
      <h3>DF: When 
        I was around 13 I started getting into animation.</h3>
      <h3>KJ: What 
        obstacles did you face when first trying to get a job in animation?<br>
        </h3>
      <h3>DF: Well 
        there weren't really many obstacles at all. Luckly I was hired right out 
        of high school to Hanna Barberra.</h3>
      <h3>KJ: Did you 
        receive any education in art/drawing/animation?<br>
        </h3>
      <h3>DF: No, I 
        was hired right out of High School.</h3>
      <h3>KJ: You've 
        made a few comic appearances such as Mumbler, Maxx #30 and various Cow 
        and Chicken covers, would you ever consider working in comics more often?<br>
        </h3>
      <h3>DF: Sure 
        I would, if I had the opportunity.</h3>
      <h3>KJ: What 
        do you enjoy most out of making cartoons?<br>
        </h3>
      <h3>DF: I really 
        like seeing people laugh and making people laugh.</h3>
      <h3>KJ: Can you 
        name some of your influences?<br>
        </h3>
      <h3>DF: Well, 
        as far as cartoons go, the Warner Bros cartoons of the 1940s; chuck jones, 
        bob clampett, tex avery.</h3>
      <h3>KJ: When 
        do you get your ideas for shows and stories<br>
        </h3>
      <h3>DF: I get 
        a lot of ideas from my childhood, and also from my kids, and abstract 
        ideas I just think of.</h3>
      <h3>KJ: Of all 
        the projects you've worked on, which of those are you most proud of?<br>
        </h3>
      <h3>DF: Cow and 
        Chicken and I am Weasel</h3>
      <h3>KJ: What 
        is your opinion on the recent degrading in animation?<br>
        </h3>
      <h3>DF: Well 
        its hard to say, I saw Shrek the other day and I thought it was dynamite, 
        with the computer animation.</h3>
      <h3>KJ: When 
        working for a big network like Cartoon Network, Do you find it hard to 
        bring out your own ideas?<br>
        </h3>
      <h3>DF: No, they 
        were pretty cool about letting me use my own ideas.</h3>
      <h3>KJ: What 
        are your current projects?<br>
        </h3>
      <h3>DF: Well 
        I have 3 shows in development, but I'm not working on a series right now, 
        I'm doing freelance work. But I do have a commercial deal with Wild Brain, 
        two of which are out. Frito-lay was really pleased with how the commercials 
        turned out and they would like to do more.</h3>
      <h3>KJ: What 
        would you be doing had you not ventured into animation?<br>
        </h3>
      <h3>DF: I really 
        don't know. I would probably be doing illustration. I don't see myself 
        working outside of the art field.</h3>
      <h3>KJ: If you 
        could be President for a day, what would you do?<br>
        </h3>
      <h3>DF: I don't 
        know. I'd really like to say something witty and intelligent here but 
        theres just so many problems. I would clean up the beuacracy and red tape 
        and garbage in Washington though.<br>
        </h3>
